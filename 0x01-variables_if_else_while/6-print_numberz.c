#include <stdio.h>

/**
 * main - Entry point of the program
 *
 * Description: This program prints all single-digit numbers of base 10
 *              starting from 0, followed by a new line. It uses the putchar
 *              function to print the digits and adheres to the Betty coding
 *              style and documentation style.
 *
 * Return: Always 0 (indicating success)
 */
int main(void)
{
	putchar('0');
	putchar('\n');

	putchar('1');
	putchar('2');
	putchar('3');
	putchar('4');
	putchar('5');
	putchar('6');
	putchar('7');
	putchar('8');
	putchar('9');
	putchar('\n');

	return (0);
}

